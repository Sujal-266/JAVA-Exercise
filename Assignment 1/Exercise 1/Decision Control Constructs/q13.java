//13. Write a program to check the divisibility of a number by 7.//

public class q13 {
    public static void main(String[] args) {
        int num = 49;
        if (num % 7 == 0) {
            System.out.println(num + " is divisible by 7");
        } else {
            System.out.println(num + " is not divisible by 7");
        }
    }
}
