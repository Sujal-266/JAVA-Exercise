//14. Write a program to check the even/odd.//

public class q14 {
	public static void main (String [] args) {
		int i = 2;
		if (i % 2 == 0) {
		System.out.println(""+i); 
        System.out.println("is even");
		} else {
			System.out.println(""+i);
			System.out.println("is odd");
		}
	}
}
			
