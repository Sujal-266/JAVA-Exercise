//2. Write a program to iterate over the array and print its elements. (Use for loop).//

public class q2 {
    public static void main(String[] args) {
        int[] array = {1, 2, 3, 4, 5};

        System.out.println("Iterate over the array and printing its elements using for loop");
        for (int i = 0; i < array.length; i++) {
            System.out.println(array[i]);
        }
    }
}
