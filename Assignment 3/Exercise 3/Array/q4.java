//4. Write a program to iterate over the array and print its elements. (Use for each loop).//

public class q4{
    public static void main(String[] args) {
        int[] array = {1, 2, 3, 4, 5};

        System.out.println("Iterate over the array and printing its elements using for each loop");
        for (int element : array) {
            System.out.println(element);
        }
    }
}
